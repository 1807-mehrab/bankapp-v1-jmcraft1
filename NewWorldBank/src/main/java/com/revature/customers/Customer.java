package com.revature.customers;

import com.revature.interfaces.CustomerActions;

public abstract class Customer implements CustomerActions {
	
	protected String fName, lName, address, city, state, zip, phone, email, password;

	public Customer(String fName, String lName, String address, String city, String state, String zip, String phone,
			String email, String password) {
		super();
		this.fName = fName;
		this.lName = lName;
		this.address = address;
		this.city = city;
		this.state = state;
		this.zip = zip;
		this.phone = phone;
		this.email = email;
		this.password = password;
	}

	public String getfName() {
		return fName;
	}

	public void setfName(String fName) {
		this.fName = fName;
	}

	public String getlName() {
		return lName;
	}

	public void setlName(String lName) {
		this.lName = lName;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getZip() {
		return zip;
	}

	public void setZip(String zip) {
		this.zip = zip;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public abstract boolean deposit(double fundsToAdd);
	
	public abstract boolean withdraw(double fundsToSubtract);
	
	public abstract double checkBalance();
	
	public abstract boolean transfer(long acct_from, long acct_to);
	
	public abstract boolean updateProfile(String email);

}
